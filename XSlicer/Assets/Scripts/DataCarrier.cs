using UnityEngine;

public class DataCarrier : MonoBehaviour
{
    public string url;
    public static DataCarrier Instance { get; private set; }

    private void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }

        Instance = this;
        DontDestroyOnLoad(gameObject);
    }
}